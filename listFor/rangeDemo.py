""" rangeDemo.py
    demonstrates how range object
    creates automatic lists of numbers
    3/28/06 """

print("range(3)")
for i in range(3):
  print(i)
print()


print("range(1, 3)")
for i in range(1, 3):
  print(i)
print()


print("range(2, 5)")
for i in range(2, 5):
  print(i)
print()

print("range(0, 55, 5)")
for i in range(0, 55, 5):
  print(i)
print()

print("range(50, 0, -5)")
for i in range(50, 0, -5):
  print(i)
print()
